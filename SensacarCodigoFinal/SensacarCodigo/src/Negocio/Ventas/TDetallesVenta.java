package Negocio.Ventas;

import Integración.Ventas.TVenta;
import Integración.Cliente.TCliente;

import java.util.List;
import Integración.Modelos.TModelo;

public class TDetallesVenta {

	private TVenta venta;

	private TCliente cliente;

	private List<TModelo> modelos;

	public TDetallesVenta(TVenta venta, TCliente cliente, List<TModelo> modelos) {
		setVenta(venta);
		setCliente(cliente);
		setModelos(modelos);
	}

	public TVenta getVenta() {
		return this.venta;
	}

	public TCliente getCliente() {
		return this.cliente;
	}

	public List<TModelo> getModelos() {
		return this.modelos;
	}

	public void setVenta(TVenta venta) {
		this.venta = venta;
	}

	public void setCliente(TCliente cliente) {
		this.cliente = cliente;
	}

	public void setModelos(List<TModelo> modelos) {
		this.modelos = modelos;
	}
}